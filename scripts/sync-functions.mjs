// Copies a subset of src/core/ into supabase/functions/_shared/core/ so the
// Deno edge functions can share the same pure-TS game logic as the web app.
// Deno requires explicit file extensions in relative specifiers, so this
// script rewrites `from './x'` -> `from './x.ts'` on copy.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

const SRC_ROOT = path.join(repoRoot, 'src', 'core');
const DEST_ROOT = path.join(repoRoot, 'supabase', 'functions', '_shared', 'core');

// Individual files copied as-is (relative to SRC_ROOT), plus directories
// walked recursively for every non-test .ts file they contain.
const SOURCE_FILES = ['cards.ts', 'evaluator.ts', 'handNotation.ts', 'potOdds.ts'];
const SOURCE_DIRS = ['ranges', 'game', 'online', 'ai'];

// クライアント専用ファイル（SRC_ROOT 相対）。solverSeries.ts は src/data/ の JSON を
// import しており Deno 側で解決できない。Edge Function 版 Analyzer はスコープ外
// （docs/SOLVER-REVIEW-DESIGN.md §12.5）のため同期しない。
const EXCLUDE_FILES = new Set(['ranges/solverSeries.ts']);

const HEADER = '// AUTO-GENERATED by sync:functions. Do not edit.\n';

// Matches `from '<relative-specifier>'` (covers import/export/export-*/type
// variants, since they all end in a `from '...'` clause).
const RELATIVE_FROM_RE = /from\s+(['"])(\.\.?\/[^'"]+)\1/g;

function hasExtension(specifier) {
  const lastSegment = specifier.split('/').pop();
  return /\.[^./]+$/.test(lastSegment);
}

function rewriteRelativeImports(content) {
  return content.replace(RELATIVE_FROM_RE, (match, quote, specifier) => {
    if (hasExtension(specifier)) return match;
    return `from ${quote}${specifier}.ts${quote}`;
  });
}

function isTestFile(fileName) {
  return fileName.endsWith('.test.ts');
}

function listTsFilesRecursive(dir) {
  if (!fs.existsSync(dir)) return [];
  const result = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      result.push(...listTsFilesRecursive(fullPath));
    } else if (entry.isFile() && entry.name.endsWith('.ts') && !isTestFile(entry.name)) {
      result.push(fullPath);
    }
  }
  return result;
}

function copyFile(absSrcPath) {
  const relPath = path.relative(SRC_ROOT, absSrcPath);
  const destPath = path.join(DEST_ROOT, relPath);
  const original = fs.readFileSync(absSrcPath, 'utf8');
  const transformed = HEADER + rewriteRelativeImports(original);
  fs.mkdirSync(path.dirname(destPath), { recursive: true });
  fs.writeFileSync(destPath, transformed, 'utf8');
}

function main() {
  if (!fs.existsSync(SRC_ROOT)) {
    console.error(`sync:functions failed: source directory not found: ${SRC_ROOT}`);
    process.exit(1);
  }

  fs.rmSync(DEST_ROOT, { recursive: true, force: true });
  fs.mkdirSync(DEST_ROOT, { recursive: true });

  const filesToCopy = [];

  for (const fileName of SOURCE_FILES) {
    const absPath = path.join(SRC_ROOT, fileName);
    if (!fs.existsSync(absPath)) {
      console.error(`sync:functions failed: expected file not found: ${absPath}`);
      process.exit(1);
    }
    filesToCopy.push(absPath);
  }

  for (const dirName of SOURCE_DIRS) {
    const absDir = path.join(SRC_ROOT, dirName);
    filesToCopy.push(...listTsFilesRecursive(absDir));
  }

  const syncedFiles = filesToCopy.filter(
    (absPath) => !EXCLUDE_FILES.has(path.relative(SRC_ROOT, absPath).split(path.sep).join('/')),
  );

  for (const absPath of syncedFiles) {
    copyFile(absPath);
  }

  console.log(`synced ${syncedFiles.length} files to supabase/functions/_shared/core/`);
}

main();
